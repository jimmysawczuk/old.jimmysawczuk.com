<h2 id="wooter">Wooter!</h2>
Wooter! is a quick little app that sits in your system tray and lets you know when the item on Woot.com changes. You can customize the delay between checks if it's a Woot Off and it serves as a quick link to the site as well. Woot Off checking should happen automatically. <strike>Also, just for chuckles, it puts changes in your Event Logs.</strike> Not anymore, at least not on Windows 7. It still tries though.

