<h2 id="sleeptimer">SleepTimer</h2>

SleepTimer is a quick little Windows app that lets users set a timer to shut down or restart their computer, or even shut down individual programs. This allows users to, for example, go to sleep while listening to their iTunes library and their computer shuts off for them after they've fallen asleep.

